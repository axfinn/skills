def main():
    os.makedirs(AUDIO_DIR, exist_ok=True)
    
    print(f"使用语音: {VOICE}")
    print("生成高清TTS音频...\n")
    
    for seg_id, text in TTS_SEGMENTS:
        aiff_path = os.path.join(AUDIO_DIR, f"{seg_id}.aiff")
        mp3_path = os.path.join(AUDIO_DIR, f"{seg_id}.mp3")
        
        print(f"生成: {seg_id}")
        
        # 使用macOS say命令生成AIFF
        subprocess.run(['say', '-v', VOICE, '-o', aiff_path, text], check=True)
        
        # 转换为高质量MP3
        subprocess.run([FFMPEG, '-y', '-i', aiff_path, 
                       '-acodec', 'libmp3lame', '-ab', '192k',  # 更高比特率
                       '-ar', '44100',  # 标准采样率
                       mp3_path],
                      capture_output=True)
        
        # 删除AIFF
        os.remove(aiff_path)
    
    print(f"\n✓ 所有高清音频已生成到 {AUDIO_DIR}")
    
    # 列出生成的文件
    total_size = 0
    for f in sorted(os.listdir(AUDIO_DIR)):
        if f.endswith('.mp3'):
            path = os.path.join(AUDIO_DIR, f)
            size = os.path.getsize(path)
            total_size += size
            print(f"  {f}: {size/1024:.1f}KB")
    
    print(f"\n总计: {total_size/1024/1024:.1f}MB")


if __name__ == "__main__":
    main()
